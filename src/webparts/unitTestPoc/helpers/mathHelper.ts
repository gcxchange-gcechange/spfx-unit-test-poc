export function add(a: number, b: number): number {
  return a + b;
}

export function multiply(a: number, b: number): number {
  return a * b;
}

export function isEven(a: number): boolean {
    return a % 2 === 0;
}

export function isOdd(a: number): boolean {
    return a % 2 === 1;
}